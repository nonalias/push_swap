#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*output;
	int		size;
	int		i;

	size = ft_strlen(s);
	output = malloc(sizeof(char) * (size + 1));
	if (!output)
		return (NULL);
	i = 0;
	while (i < size)
	{
		output[i] = s[i];
		i++;
	}
	output[i] = '\0';
	return (output);
}
